    private void btnActualizarProdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarProdActionPerformed
        String nombre = txtNombre.getText().trim();

        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el nombre del producto a actualizar.");
            return;
        }

        String sql = "UPDATE MUE_PRODUCTOS SET prod_precio = ?, prod_stock = ?, prod_IVA = ?, " +
                     "prod_alto = ?, prod_ancho = ?, prod_profundidad = ?, mat_id = ?, cat_id = ? " +
                     "WHERE LOWER(prod_nombre) = LOWER(?)";

        try (Connection con = BD.conectar();
             PreparedStatement ps = con.prepareStatement(sql)) {

            ps.setDouble(1, Double.parseDouble(txtPrecio.getText()));
            ps.setInt(2, Integer.parseInt(txtStock.getText()));
            ps.setString(3, cmbIVA.getSelectedItem().toString());
            ps.setInt(4, Integer.parseInt(txtAlto.getText()));
            ps.setInt(5, Integer.parseInt(txtAncho.getText()));
            ps.setInt(6, Integer.parseInt(txtProfundidad.getText()));
            ps.setInt(7, cmbMaterial.getSelectedIndex() + 1);
            ps.setInt(8, cmbCategoria.getSelectedIndex() + 1);
            ps.setString(9, nombre);

            int filas = ps.executeUpdate();
            if (filas > 0) {
                JOptionPane.showMessageDialog(null, "Producto actualizado.");
                cargarProductos();
            } else {
                JOptionPane.showMessageDialog(null, "No se encontró el producto para actualizar.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al actualizar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnActualizarProdActionPerformed

