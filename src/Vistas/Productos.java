    private void cargarProductos() {
        DefaultTableModel modelo = (DefaultTableModel) tblProductos.getModel();
        modelo.setRowCount(0); // Limpiar tabla

        String sql = "SELECT p.prod_id, p.prod_nombre, p.prod_precio, p.prod_stock, " +
             "(p.prod_alto || 'x' || p.prod_ancho || 'x' || p.prod_profundidad) AS dimensiones, " +
             "m.mat_nombre, c.cat_nombre, p.prod_estado, p.prod_iva, " +
             "(SELECT COUNT(*) FROM MUE_DETALLES_FACTURAS df WHERE df.prod_id = p.prod_id) AS en_factura " +
             "FROM MUE_PRODUCTOS p " +
             "JOIN MUE_MATERIALES_PRODUCTOS m ON p.mat_id = m.mat_id " +
             "JOIN MUE_CATEGORIAS c ON p.cat_id = c.cat_id";

        try (Connection con = BD.conectar();
             Statement stmt = con.createStatement();
             ResultSet rs = stmt.executeQuery(sql)) {

            while (rs.next()) {
                String estado;
                if (rs.getInt("en_factura") > 0) {
                    estado = "I";
                } else {
                    estado = rs.getString("prod_estado").equals("A") ? "A" : "I";
                }

                Object[] fila = {
                    rs.getInt("prod_id"),
                    rs.getString("prod_nombre"),
                    rs.getDouble("prod_precio"),
                    rs.getInt("prod_stock"),
                    rs.getString("mat_nombre"),
                    rs.getString("cat_nombre"),
                    rs.getString("dimensiones"),
                    estado,
                    rs.getString("prod_iva")
                };
                modelo.addRow(fila);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al cargar productos: " + e.getMessage());
        }
    }




