    private void btnAgregarProductosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarProductosActionPerformed
        try (Connection con = BD.conectar()) {
            String sql = "INSERT INTO MUE_PRODUCTOS (prod_id, prod_nombre, prod_precio, prod_stock, " +
                        "prod_IVA, prod_estado, prod_alto, prod_ancho, prod_profundidad, mat_id, cat_id) " +
                        "VALUES (prod_id_seq.NEXTVAL, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";


            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, txtNombre.getText());
            ps.setDouble(2, Double.parseDouble(txtPrecio.getText()));
            ps.setInt(3, Integer.parseInt(txtStock.getText()));
            ps.setString(4, cmbIVA.getSelectedItem().toString()); // 'S' o 'N'
            ps.setString(5, "A"); // Estado por defecto
            ps.setInt(6, Integer.parseInt(txtAlto.getText()));
            ps.setInt(7, Integer.parseInt(txtAncho.getText()));
            ps.setInt(8, Integer.parseInt(txtProfundidad.getText()));
            ps.setInt(9, cmbMaterial.getSelectedIndex());  // Asume que ID comienza en 1
            ps.setInt(10, cmbCategoria.getSelectedIndex());

            int filas = ps.executeUpdate();

            if (filas > 0) {
                JOptionPane.showMessageDialog(null, "Producto registrado correctamente.");
                cargarProductos();
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo registrar el producto.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAgregarProductosActionPerformed

