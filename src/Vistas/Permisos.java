    private void cargarPermisos() {
    DefaultTableModel modelo = (DefaultTableModel) tablaPermiso.getModel();
    modelo.setRowCount(0);
    String sql = "SELECT per_id, per_nombre, per_descripcion FROM MUE_PERMISOS_EMPLEADOS";
    try (Connection cn = BD.conectar();
         PreparedStatement ps = cn.prepareStatement(sql);
         ResultSet rs = ps.executeQuery()) {
        
        while (rs.next()) {
            modelo.addRow(new Object[]{
                rs.getString("per_id"),
                rs.getString("per_nombre"),
                rs.getString("per_descripcion")
           
            });
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al cargar permisos: " + e.getMessage());
    }
}


